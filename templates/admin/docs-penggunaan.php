    <div class="wp-desa-card" style="padding: 30px; margin-bottom: 25px;">
        <h2 style="margin-top: 0; color: #1e293b; font-size: 1.5em;">Cara Penggunaan WP Desa</h2>
        <p style="color: #64748b; line-height: 1.8;">
            WP Desa adalah plugin WordPress untuk mengelola administrasi desa secara digital.
            Plugin ini menyediakan fitur pendataan penduduk, layanan surat, pengaduan warga,
            keuangan desa, program bantuan, serta berbagai shortcode untuk ditampilkan di halaman publik.
        </p>
    </div>

    <div class="wp-desa-card" style="padding: 30px; margin-bottom: 25px;">
        <h3 style="margin-top: 0; color: #1e293b;">1. Data Penduduk</h3>
        <p style="color: #64748b; line-height: 1.8;">
            Kelola data kependudukan desa — tambah, edit, hapus, dan cari data penduduk.
            Data yang tersimpan meliputi NIK, nama, alamat, jenis kelamin, tanggal lahir, pekerjaan,
            status perkawinan, dan nomor KK.
        </p>
        <ul style="color: #475569; line-height: 2;">
            <li>Klik <strong>Data Penduduk</strong> di menu navigasi atas.</li>
            <li>Gunakan tombol <strong>Tambah Penduduk</strong> untuk menambahkan data baru.</li>
            <li>Gunakan fitur <strong>Import CSV</strong> untuk mengimpor data massal.</li>
            <li>Data dapat dicari berdasarkan NIK, nama, atau alamat.</li>
        </ul>
    </div>

    <div class="wp-desa-card" style="padding: 30px; margin-bottom: 25px;">
        <h3 style="margin-top: 0; color: #1e293b;">2. Layanan - Surat</h3>
        <p style="color: #64748b; line-height: 1.8;">
            Kelola jenis surat, permohonan surat dari warga, dan cetak surat resmi.
            Warga dapat mengajukan surat melalui shortcode <code>[wp_desa_layanan]</code> di halaman publik.
        </p>
        <ul style="color: #475569; line-height: 2;">
            <li>Buka menu <strong>Layanan &gt; Surat</strong>.</li>
            <li>Kelola <strong>Jenis Surat</strong> terlebih dahulu (contoh: SKTM, SKU, Surat Keterangan Domisili).</li>
            <li>Lihat <strong>Permohonan</strong> masuk, update status, dan cetak surat.</li>
        </ul>
    </div>

    <div class="wp-desa-card" style="padding: 30px; margin-bottom: 25px;">
        <h3 style="margin-top: 0; color: #1e293b;">3. Layanan - Aduan</h3>
        <p style="color: #64748b; line-height: 1.8;">
            Warga dapat mengirimkan aspirasi atau pengaduan melalui shortcode <code>[wp_desa_aduan]</code>.
            Admin dapat menanggapi setiap aduan dan mengubah statusnya.
        </p>
        <ul style="color: #475569; line-height: 2;">
            <li>Buka menu <strong>Layanan &gt; Aduan</strong>.</li>
            <li>Lihat semua aduan yang masuk, filter berdasarkan status.</li>
            <li>Berikan <strong>tanggapan</strong> pada setiap aduan.</li>
            <li>Ubah status: Menunggu &rarr; Diproses &rarr; Selesai / Ditolak.</li>
        </ul>
    </div>

    <div class="wp-desa-card" style="padding: 30px; margin-bottom: 25px;">
        <h3 style="margin-top: 0; color: #1e293b;">4. Keuangan</h3>
        <p style="color: #64748b; line-height: 1.8;">
            Catat dan tampilkan transparansi keuangan desa (APBDes).
            Data ditampilkan ke publik melalui shortcode <code>[wp_desa_keuangan]</code>.
        </p>
        <ul style="color: #475569; line-height: 2;">
            <li>Buka menu <strong>Keuangan &gt; Keuangan</strong>.</li>
            <li>Input data <strong>Pendapatan</strong> dan <strong>Belanja</strong> per tahun anggaran.</li>
            <li>Dashboard otomatis menampilkan ringkasan dan grafik.</li>
        </ul>
    </div>

    <div class="wp-desa-card" style="padding: 30px; margin-bottom: 25px;">
        <h3 style="margin-top: 0; color: #1e293b;">5. Keuangan - Bantuan</h3>
        <p style="color: #64748b; line-height: 1.8;">
            Kelola program bantuan sosial desa dan daftar penerima manfaat.
            Warga dapat melihat program bantuan melalui shortcode <code>[wp_desa_bantuan]</code>.
        </p>
        <ul style="color: #475569; line-height: 2;">
            <li>Buka menu <strong>Keuangan &gt; Bantuan</strong>.</li>
            <li>Buat <strong>Program Bantuan</strong> baru (BLT, PKH, dll).</li>
            <li>Tambahkan <strong>Penerima</strong> ke setiap program.</li>
        </ul>
    </div>

    <div class="wp-desa-card" style="padding: 30px; margin-bottom: 25px;">
        <h3 style="margin-top: 0; color: #1e293b;">6. Pengaturan</h3>
        <p style="color: #64748b; line-height: 1.8;">
            Atur identitas dan informasi desa yang akan digunakan untuk kop surat dan tampilan publik.
        </p>
        <ul style="color: #475569; line-height: 2;">
            <li><strong>Identitas & Kontak</strong> — Nama desa, kecamatan, kabupaten, alamat, email, telepon.</li>
            <li><strong>Logo & Media</strong> — Upload logo kabupaten untuk kop surat.</li>
            <li><strong>Kepala Desa</strong> — Nama, NIP, dan foto kepala desa.</li>
            <li><strong>Pengaturan Sistem</strong> — Development mode untuk generate dummy data.</li>
        </ul>
    </div>
</div>
